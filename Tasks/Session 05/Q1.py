#Take a string input from the user
name=input("Enter a string: ")

#The first character of the string can be accessed using index 0 (positive indexing)
print("The first character is:", name[0])

#The last character of the string can be accessed using index -1 (negative indexing)
print("The last character is:", name[-1])

#The 3rd character from the start can be accessed using index 2 (positive indexing)
print("The 3rd character from the start is:", name[2])

#The 2nd character from the end can be accessed using index -2 (negative indexing)
print("The 2nd character from the end is:", name[-2])