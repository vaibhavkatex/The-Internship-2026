
# Creating a Set
fruits = {'apple', 'banana', 'mango'}

# Perform the following operations and print the set after each step:

# a) Add 'orange' using add().
fruits.add('orange')
print(fruits)

# b) Add 'banana' again (observe the result).
fruits.add('banana')

# c) Remove 'mango' using remove().
fruits.remove('mango')
print(fruits)

# d) Try to remove 'grape' using discard() (no error should occur).
fruits.discard('grape')
print(fruits)
